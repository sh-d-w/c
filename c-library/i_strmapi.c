#include "clibrary.h"

char	*i_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	x;
	char			*t;

	x = -1;
	if ((t = i_strdup(s)))
		while (s[++x])
			t[x] = f(x, s[x]);
	return (t);
}
