#include "clibrary.h"

char	*i_strchr(const char *s, int c)
{
	int		i;

	i = -1;
	while (s[++i] && s)
		if ((char)s[i] == (char)c)
			return ((char *)s + i);
	return (((char)c == s[i]) ? ((char *)s + i) : NULL);
}
