#include "clibrary.h"

char	*i_strrchr(const char *s, int c)
{
	char	*fct;

	fct = NULL;
	while (*s && ((*s == (char)c && (fct = (char *)s)) || 1))
		s++;
	return ((*s == (char)c) ? (char *)s : fct);
}
