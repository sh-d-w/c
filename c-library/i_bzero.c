#include "clibrary.h"

void	i_bzero(void *s, size_t n)
{
	size_t			cmp;
	unsigned char	*ptr;

	cmp = 0;
	ptr = (unsigned char *)s;
	while (n > 0 && cmp < n)
		ptr[++cmp - 1] = 0;
}
